export type InsertGame = typeof game.$inferInsert;
export type InsertPlayer = typeof player.$inferInsert;
export type InsertRound = typeof round.$inferInsert;
export type InsertRoundScore = typeof roundScore.$inferInsert;

export type SelectUser = typeof user.$inferSelect;
export type SelectGame = typeof game.$inferSelect;
export type SelectPlayer = typeof player.$inferSelect;
export type SelectRound = typeof round.$inferSelect;
export type SelectRoundScore = typeof roundScore.$inferSelect;